    private void tbPhongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbPhongMouseClicked
        int is = tbPhong.getSelectedRow();
        TableModel model = tbPhong.getModel();
        PhongDTO phong = PhongBUS.getPhong(Integer.parseInt(model.getValueAt(is, 0).toString()));

        lbMaPhong.setText("" + phong.getMaPhong());
        lbTenPhong.setText(phong.getTenPhong());
        loadcbLoaiPhong();
        for (int j = 0; j < cbLoaiPhong.getItemCount(); j++) {
            Object item = cbLoaiPhong.getItemAt(j);
            int value = ((ComboItem) item).getValue();
            if (phong.getLoaiPhong() == value) {
                cbLoaiPhong.setSelectedIndex(j);
            }
        }
        loadcbTinhTrang();
        for (int j = 0; j < cbTinhTrang.getItemCount(); j++) {
            Object item = cbTinhTrang.getItemAt(j);
            int value = ((ComboItem) item).getValue();
            if (phong.getTinhTrang() == value) {
                cbTinhTrang.setSelectedIndex(j);
            }
        }
        lbGiaGioNgay.setText("" + phong.getGiaGioNgay());
        lbGiaGioDem.setText("" + phong.getGiaGioDem());
        if (lbMaPhong.getText() == null || ((ComboItem) cbTinhTrang.getSelectedItem()).getValue() == 2 || ((ComboItem) cbTinhTrang.getSelectedItem()).getValue() == 3) {
            btUpdate.setEnabled(false);
        } else {
            btUpdate.setEnabled(true);
        }
    }//GEN-LAST:event_tbPhongMouseClicked

