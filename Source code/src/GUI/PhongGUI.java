    public void loadPhongAll() {
        String[] header = {"Mã phòng", "Tên phòng", "Loại phòng", "Tình trạng", "Giá giờ ngày", "Giá giờ đêm"};
        dtm = new DefaultTableModel(header, 0);
        ArrayList<PhongDTO> arr;
        arr = PhongBUS.PhongAll();
        PhongDTO phong;
        for (int i = 0; i < arr.size(); i++) {
            phong = arr.get(i);
            int x = phong.getMaPhong();
            int MaPhong = x;
            String TenPhong = phong.getTenPhong();
            int MaLP = phong.getLoaiPhong();
            String LoaiPhong = LoaiPhongBUS.getLoaiPhong(MaLP).getLoaiPhong();
            int MaTTP = phong.getTinhTrang();
            String TinhTrang = TinhTrangPhongBUS.getTinhTrangPhong(MaTTP).getTenTTP();
            double GiaGioNgay = phong.getGiaGioNgay();
            double GiaGioDem = phong.getGiaGioDem();
            Object[] row = {MaPhong, TenPhong, LoaiPhong, TinhTrang, GiaGioNgay, GiaGioDem};
            dtm.addRow(row);
        }
        tbPhong.setModel(dtm);
    }

    public void loadcbLoaiPhong() {
        ArrayList<LoaiPhongDTO> arr = LoaiPhongBUS.LoaiPhongAll();
        LoaiPhongDTO dv;
        cbLoaiPhong.removeAllItems();
        for (int i = 0; i < arr.size(); i++) {
            dv = arr.get(i);
            String TenLDV = dv.getLoaiPhong();
            int MaLDV = dv.getMaLP();
            cbLoaiPhong.addItem(new ComboItem(TenLDV, MaLDV));
        }
    }

    public void loadcbTinhTrang() {
        ArrayList<TinhTrangPhongDTO> arr = TinhTrangPhongBUS.TinhTrangPhong_All();
        TinhTrangPhongDTO dv;
        cbTinhTrang.removeAllItems();
        for (int i = 0; i < arr.size(); i++) {
            dv = arr.get(i);
            String TenLDV = dv.getTenTTP();
            int MaLDV = dv.getMaTTP();
            cbTinhTrang.addItem(new ComboItem(TenLDV, MaLDV));
        }
    }

