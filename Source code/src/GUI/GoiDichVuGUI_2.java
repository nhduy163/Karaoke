    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        if (GoiDichVuBUS.checkSoLuong(tfSoLuong)) {
            QuanLyDatTiecGUI mhc = (QuanLyDatTiecGUI) this.getParent();
            int MaGDV = GoiDichVuBUS.getSoGoiDV() + 1;
            int MaDV = Integer.parseInt(lbMaDV.getText());
            int SL = Integer.parseInt(tfSoLuong.getText());
            GoiDichVuDAO gdv = new GoiDichVuDAO();
            boolean chk = gdv.InsertGoiDV(MaGDV, mhc.getMaTP(), MaDV, SL);
            if (chk == true) {
                ThuePhongDAO tp = new ThuePhongDAO();
                double TienTraTruoc = ThuePhongBUS.getTienTraTruoc(mhc.getMaTP());
                tp.UpdateTienTraTruoc(mhc.getMaTP(), TienTraTruoc);
                mhc.loadtbDichVu(mhc.getMaTP());
                JOptionPane.showMessageDialog(this, DichVuBUS.getDichVu(MaDV).getTenDV() + ":   " + SL);
            } else {

            }
        } else {
            JOptionPane.showMessageDialog(null, "Số lượng là số tự nhiên lớn hơn 0!");
        }
    }//GEN-LAST:event_btThemActionPerformed

