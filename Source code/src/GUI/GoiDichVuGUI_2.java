    private void tbGoiDVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbGoiDVMouseClicked
        int is = tbGoiDV.getSelectedRow();
        TableModel model = tbGoiDV.getModel();
        DichVuDTO dv = DichVuBUS.getDichVu(Integer.parseInt(model.getValueAt(is, 0).toString()));
        lbMaDV.setText("" + dv.getMaDV());
        lbTenDV.setText(dv.getTenDV());
        Blob img = dv.getHinhAnh();
        if (img != null) {
            int blobLength;
            byte[] blobAsBytes = null;
            try {
                blobLength = (int) img.length();
                blobAsBytes = img.getBytes(1, blobLength);
            } catch (SQLException ex) {
                Logger.getLogger(DichVuGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            lbHinhAnh.setIcon(ResizeImage(blobAsBytes));
        } else {
            lbHinhAnh.setIcon(null);
        }
        if (lbMaDV.getText() == null) {
            btThem.setEnabled(false);
        } else {
            btThem.setEnabled(true);
        }
    }//GEN-LAST:event_tbGoiDVMouseClicked

    private void cbLoaiDVItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbLoaiDVItemStateChanged
        loadDichVuAll();
        if (cbLoaiDV.getSelectedItem() != null) {
            Object item = cbLoaiDV.getSelectedItem();
            int LoaiDV = ((ComboItem) item).getValue();
            if (LoaiDV == 1) {
                loadDichVu();
            } else {
                loadDichVu_Loai(LoaiDV);
            }
        }
    }//GEN-LAST:event_cbLoaiDVItemStateChanged

