    private void tbHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbHoaDonMouseClicked
        int is = tbHoaDon.getSelectedRow();
        TableModel model = tbHoaDon.getModel();
        HoaDonDTO nv = HoaDonBUS.getHoaDon(Integer.parseInt(model.getValueAt(is, 0).toString()));

        lbMaHD.setText("" + nv.getMaHD());
        lbKhachHang.setText(KhachHangBUS.getKhachHang(ThuePhongBUS.getThuePhong(nv.getMaTP()).getMaKH()).getTenKH());
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
        lbNgayHD.setText(dateFormat.format(nv.getNgayHD()));
        lbNhanVien.setText(NhanVienBUS.getNhanVien(nv.getMaNV()).getTennv());
        lbTongTienDV.setText("" + nv.getTienDichVu());
        DateFormat dateFormat2 = new SimpleDateFormat("HH:mm:ss dd/MM/yyyy");
        lbGioBatDau.setText(dateFormat2.format(ThuePhongBUS.getThuePhong(nv.getMaTP()).getGioBatDau()));
        lbGioKetThuc.setText(dateFormat2.format(ThuePhongBUS.getThuePhong(nv.getMaTP()).getGioKetThuc()));
        lbTienGio.setText("" + nv.getTienGio());
        lbGiamGia.setText("" + nv.getGiamGia());
        lbTongTien.setText("" + nv.getTongTien());
        loadChiTietDV(nv.getMaTP());
    }//GEN-LAST:event_tbHoaDonMouseClicked

