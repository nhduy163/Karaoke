    public void loadChiTietDV(int MaTP) {
        String[] header = {"Tên dịch vụ", "Số lượng", "Đơn Giá"};
        DefaultTableModel dtm2 = new DefaultTableModel(header, 0);
        ArrayList<GoiDichVuDTO> arr = GoiDichVuBUS.GoiDichVuAll(MaTP);
        GoiDichVuDTO kh;
        for (int i = 0; i < arr.size(); i++) {
            kh = arr.get(i);
            String TenDV = DichVuBUS.getDichVu(kh.getMaDV()).getTenDV();
            int SL = kh.getSL();
            double DonGia = DichVuBUS.getDichVu(kh.getMaDV()).getDonGia();
            Object[] row = {TenDV, SL, DonGia};
            dtm2.addRow(row);
        }
        tbDichVu.setModel(dtm2);
    }

