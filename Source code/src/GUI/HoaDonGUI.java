    private void tfSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfSearchKeyReleased
        DefaultTableModel model = (DefaultTableModel) tbHoaDon.getModel();
        String query = tfSearch.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(model);
        tbHoaDon.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(query));
    }//GEN-LAST:event_tfSearchKeyReleased

    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSearchActionPerformed
        if (HoaDonBUS.checkNgay(dcbatdau)) {
            if (HoaDonBUS.checkNgay(dcketthuc)) {
                Date start = dcbatdau.getDate();
                Date end = dcketthuc.getDate();
                end.setHours(23);
                end.setMinutes(59);
                end.setSeconds(59);
                start.setHours(0);
                start.setMinutes(0);
                start.setSeconds(0);              
                Timestamp batdau = new Timestamp(start.getTime());
                Timestamp ketthuc = new Timestamp(end.getTime());
                loadHoaDon(batdau, ketthuc);
            } else {
                JOptionPane.showMessageDialog(null, "error");
                dcketthuc.requestFocusInWindow();
            }
        }else{
            JOptionPane.showMessageDialog(null, "error");
                dcbatdau.requestFocusInWindow();
        }

    }//GEN-LAST:event_btSearchActionPerformed

