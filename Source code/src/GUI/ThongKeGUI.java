    public void loadThongKeNgay(Date NgayHD) {
        ArrayList<HoaDonDTO> arr = HoaDonBUS.getHoaDon(NgayHD);
        HoaDonDTO kh;
        int rc = dtm.getRowCount();
        for (int i = 0; i < rc; i++) {
            dtm.removeRow(0);
        }
        for (int i = 0; i < arr.size(); i++) {
            kh = arr.get(i);
            int MaHD = kh.getMaHD();
            double TienGio = kh.getTienGio();
            double TienDV = kh.getTienDichVu();
            int GiamGia = kh.getGiamGia();
            double TongTien = kh.getTongTien();
            Object[] row = {MaHD, TienGio, TienDV, GiamGia, TongTien};
            dtm.addRow(row);
        }
        tbThongKeNgay.setModel(dtm);
    }

