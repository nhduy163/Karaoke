    private void btInThongKeNgayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInThongKeNgayActionPerformed
        MysqlDataAccessHelper my = new MysqlDataAccessHelper();
        Date start = dcbatdau.getDate();
        Date end = dcketthuc.getDate();
        end.setHours(23);
        end.setMinutes(59);
        end.setSeconds(59);
        start.setHours(0);
        start.setMinutes(0);
        start.setSeconds(0);
        Timestamp batdau = new Timestamp(start.getTime());
        Timestamp ketthuc = new Timestamp(end.getTime());
        try {
            my.open();
            String sql = "select *\n"
                    + " from hoadon\n"
                    + " where NgayHD >= $P{NgayBatDau} and NgayHD <= $P{NgayKetThuc}";
            HashMap<String, Object> map = new HashMap<>();
            map.put("NgayBatDau", batdau);
            map.put("NgayKetThuc", ketthuc);
            InputStream is = getClass().getResourceAsStream("report2.jrxml");

            JRDesignQuery jrDesignQuery = new JRDesignQuery();
            jrDesignQuery.setText(sql);

            JasperDesign jasperDesign = JRXmlLoader.load(is);
            jasperDesign.setQuery(jrDesignQuery);

            JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, map, my.getConn());
            JasperViewer.viewReport(jasperPrint, false);
        } catch (JRException e) {
        }
    }//GEN-LAST:event_btInThongKeNgayActionPerformed

    private void btInThongKeThangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInThongKeThangActionPerformed
        MysqlDataAccessHelper my = new MysqlDataAccessHelper();
        int month = cbThang.getSelectedIndex() + 1;
        int year = Integer.parseInt(tfNam.getText());
        try {
            my.open();
            String sql = "select sum(TongTien) as TT, day(NgayHD), month(NgayHD)\n"
                    + "from hoadon \n"
                    + "where month(NgayHD) = " + month + " and year(NgayHD) = " + year + "\n"
                    + "group by date(NgayHD)";
            Map parametersMap = new HashMap();
            InputStream is = getClass().getResourceAsStream("report1.jrxml");

            JRDesignQuery jrDesignQuery = new JRDesignQuery();
            jrDesignQuery.setText(sql);

            JasperDesign jasperDesign = JRXmlLoader.load(is);
            jasperDesign.setQuery(jrDesignQuery);

            JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parametersMap, my.getConn());
            JasperViewer.viewReport(jasperPrint, false);
        } catch (JRException e) {
        }
    }//GEN-LAST:event_btInThongKeThangActionPerformed

