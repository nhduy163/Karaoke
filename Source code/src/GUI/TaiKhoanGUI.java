    public void loadTKAll() {
        String[] header = {"Mã tài khoản", "Tên tài khoản", "Mật khẩu", "Nhân viên"};
        dtm = new DefaultTableModel(header, 0);
        ArrayList<TaiKhoanDTO> arr;
        arr = TaiKhoanBUS.TaiKhoanAll();
        TaiKhoanDTO phong;
        for (int i = 0; i < arr.size(); i++) {
            phong = arr.get(i);
            int MaTK = phong.getMaTK();
            String TenTK = phong.getTenTK();
            String MatKhau = phong.getMatKhau();
            String NhanVien = NhanVienBUS.getNhanVien(TaiKhoanBUS.getTaiKhoan(MaTK).getMaNV()).getTennv();
            Object[] row = {MaTK, TenTK, MatKhau, NhanVien};
            dtm.addRow(row);
        }
        tbTaiKhoan.setModel(dtm);
    }

