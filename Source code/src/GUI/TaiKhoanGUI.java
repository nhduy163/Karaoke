    private void tbTaiKhoanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTaiKhoanMouseClicked
        int is = tbTaiKhoan.getSelectedRow();
        TableModel model = tbTaiKhoan.getModel();
        TaiKhoanDTO phong = TaiKhoanBUS.getTaiKhoan(Integer.parseInt(model.getValueAt(is, 0).toString()));

        lbMaTK.setText("" + phong.getMaTK());
        lbTenTK.setText(phong.getTenTK());
        lbMatKhau.setText(phong.getMatKhau());
        lbNhanVien.setText(NhanVienBUS.getNhanVien(phong.getMaNV()).getTennv());

        if (Integer.parseInt(model.getValueAt(is, 0).toString()) == 1) {
            btUpDate.setEnabled(false);
            btDelete.setEnabled(false);
        } else {
            btUpDate.setEnabled(true);
            btDelete.setEnabled(true);
        }
    }//GEN-LAST:event_tbTaiKhoanMouseClicked

