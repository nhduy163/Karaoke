    private void tbKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbKhachHangMouseClicked
        int is = tbKhachHang.getSelectedRow();
        TableModel model = tbKhachHang.getModel();
        KhachHangDTO nv = KhachHangBUS.getKhachHang(Integer.parseInt(model.getValueAt(is, 0).toString()));

        lbMaKH.setText("" + nv.getMaKH());
        lbTenKH.setText(nv.getTenKH());
        lbCMND.setText("" + nv.getCMND());
        lbSDT.setText("" + nv.getSDT());
        lbTichLuy.setText("" + nv.getTichLuy());
        lbDiaChi.setText("" + nv.getDiaChi());
        loadcbLoaiKH();
        for (int j = 0; j < cbLoaiKH.getItemCount(); j++) {
            Object item = cbLoaiKH.getItemAt(j);
            int value = ((ComboItem) item).getValue();
            if (nv.getLoaiKH() == value) {
                cbLoaiKH.setSelectedIndex(j);
            }
        }

        if (lbMaKH.getText() == null) {
            btUpdate.setEnabled(false);
        } else {
            btUpdate.setEnabled(true);
        }
        if (Integer.parseInt(model.getValueAt(is, 0).toString()) == 1) {
            btUpdate.setEnabled(false);
        } else {
            btUpdate.setEnabled(true);
        }

    }//GEN-LAST:event_tbKhachHangMouseClicked

