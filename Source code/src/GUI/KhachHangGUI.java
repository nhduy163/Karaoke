    public void loadKhachHangALL() {
        String[] header = {"Mã khách hàng", "Họ tên", "CMND", "SĐT", "Địa chỉ", "Tích lũy", "Loại khách hàng"};
        dtm = new DefaultTableModel(header, 0);
        ArrayList<KhachHangDTO> arr;
        arr = KhachHangBUS.KhachHangAll();
        KhachHangDTO kh;
        for (int i = 0; i < arr.size(); i++) {
            kh = arr.get(i);
            int MaKH = kh.getMaKH();
            String TenKH = kh.getTenKH();
            String CMND = kh.getCMND();
            String SDT = kh.getSDT();
            String DiaChi = kh.getDiaChi();
            double TichLuy = kh.getTichLuy();
            String LoaiKH = KhachHangBUS.getLoaiKH(kh.getLoaiKH());
            Object[] row = {MaKH, TenKH, CMND, SDT, DiaChi, TichLuy, LoaiKH};
            dtm.addRow(row);
        }
        tbKhachHang.setModel(dtm);
    }

    public void sort() {
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(dtm);
        tbKhachHang.setRowSorter(sorter);
    }

    public void loadcbLoaiKH() {
        ArrayList<LoaiKHDTO> arr = LoaiKHBUS.LoaiKHAll();
        LoaiKHDTO dv;
        cbLoaiKH.removeAllItems();
        for (int i = 0; i < arr.size(); i++) {
            dv = arr.get(i);
            String TenLDV = dv.getLoaiKH();
            int MaLDV = dv.getMaLKH();
            cbLoaiKH.addItem(new ComboItem(TenLDV, MaLDV));
        }
    }

