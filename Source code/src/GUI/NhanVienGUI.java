    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        String Manv = lbManv.getText();
        String Tennv = lbTennv.getText();
        String NgaySinh = lbNgaySinh.getText();
        String CMND = lbCMND.getText();
        String SDT = lbSDT.getText();
        String DiaChi = lbDiaChi.getText();
        String Luong = lbLuong.getText();
        Object item = cbChucVu.getSelectedItem();
        int ChucVu = ((ComboItem) item).getValue();
        Object item2 = cbTinhTrang.getSelectedItem();
        int TinhTrang = ((ComboItem) item2).getValue();
        int GioiTinh;
        if (rdNam.isSelected()) {
            GioiTinh = 1;
        } else {
            GioiTinh = 0;
        }
        NhanVienGUI_Sua sua = null;
        try {
            sua = new NhanVienGUI_Sua(this, true, Manv, Tennv, NgaySinh, GioiTinh, CMND, SDT, DiaChi, Luong, ChucVu, TinhTrang);
        } catch (ParseException ex) {
            Logger.getLogger(NhanVienGUI.class.getName()).log(Level.SEVERE, null, ex);
        }

        sua.setVisible(true);
        loadNhanVienAll();
    }//GEN-LAST:event_btUpdateActionPerformed

