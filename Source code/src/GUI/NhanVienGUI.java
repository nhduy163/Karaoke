    public void loadNhanVienAll() {
        String[] header = {"Mã Nhân Viên", "Họ Tên", "Ngày sinh", "CMND", "SĐT", "Lương", "Tình Trạng"};
        dtm = new DefaultTableModel(header, 0);
        ArrayList<NhanVienDTO> arr;
        arr = NhanVienBUS.NhanVienAll();
        NhanVienDTO nv;
        for (int i = 0; i < arr.size(); i++) {
            nv = arr.get(i);
            int x = nv.getManv();
            int Manv = x;
            String Tennv = nv.getTennv();
            Date NgaySinh = nv.getNgaySinh();
            String CMND = nv.getCMND();
            String SDT = nv.getSDT();
            int Luong = nv.getLuong();
            String TinhTrang = NhanVienBUS.getLamViec(nv.getTinhTrang());
            Object[] row = {Manv, Tennv, NgaySinh, CMND, SDT, Luong, TinhTrang};
            dtm.addRow(row);
        }
        Table_NhanVien.setModel(dtm);
    }

    public void loadcbChucVu() {
        ArrayList<ChucVuDTO> arr = ChucVuBUS.ChucVuAll();
        ChucVuDTO dv;
        cbChucVu.removeAllItems();
        for (int i = 0; i < arr.size(); i++) {
            dv = arr.get(i);
            String TenLDV = dv.getTenCV();
            int MaLDV = dv.getMaCV();
            cbChucVu.addItem(new ComboItem(TenLDV, MaLDV));
        }
    }

    public void loadcbTinhTrang() {
        ArrayList<TinhTrangLamViecDTO> arr = TinhTrangLamViecBUS.TTLV_All();
        TinhTrangLamViecDTO dv;
        cbTinhTrang.removeAllItems();
        for (int i = 0; i < arr.size(); i++) {
            dv = arr.get(i);
            String TenLDV = dv.getTenTTLV();
            int MaLDV = dv.getMaTTLV();
            cbTinhTrang.addItem(new ComboItem(TenLDV, MaLDV));
        }
    }

