    private void Table_NhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_NhanVienMouseClicked
        int is = Table_NhanVien.getSelectedRow();
        TableModel model = Table_NhanVien.getModel();
        NhanVienDTO nv = NhanVienBUS.getNhanVien(Integer.parseInt(model.getValueAt(is, 0).toString()));
        lbManv.setText("" + nv.getManv());
        lbTennv.setText(nv.getTennv());
        lbNgaySinh.setText(nv.getNgaySinh().toString());
        lbCMND.setText("" + nv.getCMND());
        lbSDT.setText("" + nv.getSDT());
        lbDiaChi.setText(nv.getDiaChi());
        lbLuong.setText("" + nv.getLuong());
        loadcbChucVu();
        for (int j = 0; j < cbChucVu.getItemCount(); j++) {
            Object item = cbChucVu.getItemAt(j);
            int value = ((ComboItem) item).getValue();
            if (nv.getChucVu() == value) {
                cbChucVu.setSelectedIndex(j);
            }
        }
        loadcbTinhTrang();
        for (int j = 0; j < cbTinhTrang.getItemCount(); j++) {
            Object item = cbTinhTrang.getItemAt(j);
            int value = ((ComboItem) item).getValue();
            if (nv.getTinhTrang() == value) {
                cbTinhTrang.setSelectedIndex(j);
            }
        }
        int gioitinh = nv.getGioiTinh();
        if (gioitinh == 1) {
            rdNam.setSelected(true);
        }
        if (gioitinh == 0) {
            rdNu.setSelected(true);
        }
        if (lbManv.getText() == null) {
            btUpdate.setEnabled(false);
        } else {
            btUpdate.setEnabled(true);
        }
        if (Integer.parseInt(model.getValueAt(is, 0).toString()) == 1) {
            btUpdate.setEnabled(false);
        } else {
            btUpdate.setEnabled(true);
        }
    }//GEN-LAST:event_Table_NhanVienMouseClicked

